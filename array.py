
courses = ["computer science", "IT", "BIT", "ICT"]
print(courses)

# accessing an element in an array
print(courses[3])

# looping through an array
for x in courses:
    print(x)

# adding anew element in an array
courses.append("software engineering")
print(courses)

# deleting an element in an array
courses.remove("IT")
print(courses)
